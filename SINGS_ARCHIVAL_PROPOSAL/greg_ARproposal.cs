Proposal.abstract= \
     Molecular hydrogen (H2) is inherently connected with the evolution of a\
     \ galaxy through a variety of processes.  In galaxies, H2 traces star fo\
     rmation in nuclear regions, near the ends of bars in barred spirals, an\
     d along spiral arms where star formation is triggered by the passage of\
     \ a spiral density wave. The SINGS legacy program has used the Spitzer I\
     nfrared Spectrograph (IRS) long-low module (LL; 14 - 38 microns) in spe\
     ctral mapping mode to map large strips across 65 galaxies ranging in Hu\
     bble type, star-formation rate, and nuclear activity.   We propose to u\
     se the SINGS LL data cubes to map the spatial distribution of warm (T =\
     \ 100 - 300 K) H2, traced by the H2 S(0) (28.2 micron) and H2 S(1) (17.0\
     \ micron) lines, in 18 SINGS galaxies that have also been observed in CO\
     \ (J = 1 - 0) emission through the Berkeley-Illinois-Maryland Array Surv\
     ey of Nearby Galaxies (BIMA SONG).  We have developed unique software t\
     hat creates spatially resolved maps of extremely faint and blended spec\
     tral lines from Spitzer IRS LL data cubes.  Using our software, we prop\
     ose to map H2 S(0) and H2 S(1) emission across each galaxy and use thes\
     e two lines to model variations in the H2 excitation and mass across ea\
     ch galaxy.  We will distinguish the H2 excitation mechanism as a functi\
     on of position across each galaxy by comparing the H2 maps to CO emissi\
     on (from SONG) and maps of mid-infrared spectral diagnostics such as th\
     e [O IV](25.89 micron) line (a shock diagnostic) and the [S III](18.71 \
     micron)-to-[S III](33.48 micron) line ratio (an HII region electron den\
     sity diagnostic). We will also compare H2 spatial variations to UV and \
     optical imagery (U, B, V, R,  and H(alpha)) for additional insight into\
     \ the H2 excitation mechanisms and to correlate H2 properties with the a\
     ges, masses, and star formation rates of stellar clusters and associati\
     ons across the sample of SINGS galaxies.

